    private void BTNguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNguardarActionPerformed
        switch(modus){
            case 0 -> {
                int id = 0;
                int idv = 0;
                for(int i = 0; i < Libro.libros.size(); i++){
                    if(Libro.libros.get(i).getNombre().equals(Nombreentrado)){
                        idv = i;
                        id = Libro.libros.get(i).getId();
                        break;
                    }
                }
                
                new Libro().actualizar(new String[] {String.valueOf(id),Libro.libros.get(idv).getNombre() ,TFnombre.getText(), CBgenero.getSelectedItem().toString(), CBautor.getSelectedItem().toString()});
                this.dispose();
            }
            
            case 1 -> {
                int idv = 0;
                int id = 0;
                for(int i = 0; i < Autor.autores.size(); i++){
                    if(Autor.autores.get(i).getNombre().equals(Nombreentrado)){
                        idv = i;
                        id = Autor.autores.get(i).getId();
                        break;
                    }
                }
                
                new Autor().actualizar(new String[] {String.valueOf(id),Autor.autores.get(idv).getNombre() ,TFnombre.getText(), CBgenero.getSelectedItem().toString()});
                this.dispose();
            }
            
            case 2 -> {
                int idv = 0;
                for(int i = 0; i < Genero.generos.size(); i++){
                    if(Genero.generos.get(i).getNombre().equals(Nombreentrado)){
                        idv = i;
                        break;
                    }
                }
                
                new Genero().actualizar(new String[] {Genero.generos.get(idv).getNombre(), TFnombre.getText()});
                this.dispose();
            }

        }
    }//GEN-LAST:event_BTNguardarActionPerformed

