    public Modificar(int modus, String nombre) {
        initComponents();
        this.modus = modus;
        this.Nombreentrado = nombre;
        
        ArrayList<String> generos = new ArrayList<>(SQL.Buscar.generos);
        generos.add("Agregar...");
        CBgenero.setModel(new DefaultComboBoxModel(generos.toArray()));
        
        switch(modus){
            case 0 -> {
                Ltitulo.setText("Modificar libro");
                
                ArrayList<String> autores = new ArrayList<>(SQL.Buscar.autores);
                autores.add("Agregar...");
                CBautor.setModel(new DefaultComboBoxModel(autores.toArray()));
                
                TFnombre.setText(nombre);
                
                for(int i = 0; i < Libro.libros.size(); i++){
                    if(Libro.libros.get(i).getNombre().equals(nombre)){
                        CBgenero.setSelectedItem(Libro.libros.get(i).getGenero());
                        CBautor.setSelectedItem(Libro.libros.get(i).getAutor());
                    }
                }
            }
            
            case 1 -> {
                Ltitulo.setText("Modificar autor");
                Lautor.setVisible(false);
                CBautor.setVisible(false);
                
                TFnombre.setText(nombre);
                
                for(int i = 0; i < Autor.autores.size(); i++){
                    if(Autor.autores.get(i).getNombre().equals(nombre)){
                        CBgenero.setSelectedItem(Autor.autores.get(i).getGenero());
                        break;
                    }
                }
            }
            
            case 2 -> {
                Ltitulo.setText("Modificar genero");
                TFnombre.setText(nombre);
                Lgenero.setVisible(false);
                CBgenero.setVisible(false);
                Lautor.setVisible(false);
                CBautor.setVisible(false);
            }
        }
    }

